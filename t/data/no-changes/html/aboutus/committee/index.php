    <h1 class="header">The Committee</h1>

<p><strong> The club is run by a Committee of volunteers.  The duties
of each committee post are detailed below.  We are always on the look
out for new committee members, so please contact us if you would like
to find out more.</strong> </p>

<p><strong>Chairperson</strong>  Responsible for making sure that the
club is working towards its aims of supporting and celebrating
multiple birth families in Edinburgh and the Lothians.
Maintaining/increasing our contact with the local hospitals, and
health centres.  Committee meetings are held every six weeks and
preparing agendas and bringing along copies of minutes usually falls
to the Chairperson.  Roping members both on and off the committee into
helping out.  Instrumental in the orgainsation of the annual Summer
Fair and Christmas party.  </p>

<p><strong>Secretary</strong> Take minutes at committee meetings and
generally support the committee and the club throughout the year.
This would be an ideal role for anyone wanting either to join or stay
on the committee and undertake a role that is enjoyable but not too
onerous!</p>

<p><strong>Treasurer</strong> The main duties involve banking cheques,
writing cheques and updating the records to produce an income and
expenditure account for the year.</p>

<p><strong>Fundraiser</strong> Responsible for raising funds through a
variety of means.  This includes running the nearly new sales and
approaching businesses for raffle prices or donations for specific
equipment.</p>

<p><strong>Membership Secretary</strong> This role is very flexible
and can be shared by two people.  The main tasks are administration of
the Excel membership database, produce quarterly membership reports
for the newsletter, produce short updeates for each committee meeting,
manage the annual renewal of each family's membership, send out
welcome packs to our new members and put them in contact with other
members who live nearby or have twins at the same stage.  Also
responsible for collecting all the information for the monthly email
and sending it out to members.</p>

<p><strong>Social Convenor</strong> Usually filled by two committee
members. The main responsibility is to organise a range of events
throughout the year for multiples, their siblings and parents.  As
well as getting to plan the social calendar for our members, making
and confirming the booking with venues, you are also responsible for
taking payment for event bookings from members and forwarding the
monies on to our Treasurer, as well as paying deposits and balances to
venues (always promptly reimbursed by our Treasurer!).  It is a great
position for keeping your organisational skills up to scratch! A full
list of contact details for previously booked events will be provided.
</p>

<p><strong>Daytime Coordinators for local groups (currently at Comely
Bank, Joppa, Oxgangs, and South Queensferry)</strong> The role involves
opening and locking the hall, putting out the toys, welcoming
newcomers, making sure we have enough teabags, milk and biscuits,
taking money, paying for the hall, and giving the Treasurer an account
of ins and outs.  There are also posters to make sometimes and forms
to print.</p>

<p><strong>Coordinators for First-Year's groups (currently at St John's
hospital and Oxgangs)</strong> The role exists to run these
educational/social evening groups for prospective parents and parents
of multiples in their first year.  The aim is to arrange to have
different speakers every month on topics like equipment for the first
year, birth experiences, weaning, buggies and prams, etc.</p>


<p><strong>Newsletter Editor</strong> This role involves cajoling the
committee and others into writing articles, word processing the
newsletter in a standard format and submitting it on to a printer.</p>

<p><strong>Webmaster</strong> Keep the website online and up to date.
Moderate the messageboard, assisting users and setting up new
accounts.  Setup and maintain the club email accounts.</p>

