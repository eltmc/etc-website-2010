    <h1 class="header">Links</h1>
        <p>
        <strong>
        Listed below are some links on the Web which you may find useful
        </strong>
        </p>

        <h2><a href="http://www.gotyourhandsfull.com/">Twins Blog - Got Your Hands Full</a></h2>
Journalist and mother of 8 year old twin girls,  Linda Jones, started her blog as a resource all about twins, triplets and more.

        <h2><a href="http://www.bbc.co.uk/cbeebies">CBeebies</a></h2>
The BBC have kindly given us permission to link to the CBeebies site.
                                  If you haven't yet given your multiples the chance to use a computer, start
                                  here. Lots of games and things to learn involving their favourite CBeebies
                                  characters.
        <h2><a href="http://www.doula.org.uk/">Doula UK</a></h2>
&quot;Doula&quot; (pronounced &quot;doola&quot;) is a Greek word meaning
                                  &quot;woman servant or caregiver&quot;. It now refers to an experienced woman
                                  who offers emotional and practical support to a woman (or couple) before,
                                  during and after childbirth.
        <h2><a href="http://www.ethicalbabygifts.co.uk">Ethical Baby Gifts</a></h2>
An online store with a fabulous range of organic cotton baby clothes, organic cotton clothes for young children and organic skincare products.  Please mention the Edinburgh twins club when ordering.
        <h2><a href="http://www.familiesonline.co.uk">Families Online</a></h2>
Details events and outings specifically in the Edinburgh
                    area.
	 <h2><a href="http://www.kikiandbree.com/">Kiki and Bree - For all things Twins</a></h2>
Share your experiences and buy beautiful things for twins. Share, support, shop - and all without leaving the house.

        <h2><a href="http://www.littleplay.co.uk">Little Imaginations</a></h2>
Children's swimming ponchos. Please mention the Edinburgh twins club when ordering.
        <h2><a href="http://www.mumsnet.com">mumsnet</a></h2>
Produced by Parents. Great place to share ideas and get
                    tips and advice.
        <h2><a href="http://www.parents.org.uk">Parents Online</a></h2>
Run by parents. Aims to help anyone who's looking after
                    children of primary school age.
        <h2><a href="http://www.preciouslittleone.com/uk">Precious Little One</a></h2>
ood source of advice on looking after babies and toddlers.
                    Also handy shopping guides, sections devoted to education,
                    healthcare and an A-Z of babynames.
        <h2><a href="http://www.raisingkids.co.uk">Raising Kids</a></h2>
Children up to the age of 18 are catered for here. Covers
                    babies month by month development and age-old problems such
                    as &quot; Why won't my children listen to me ?&quot; and &quot;
                    How can I stop siblings fighting with each other ?&quot;
        <h2><a href="http://www.tigerchild.com">Tigerchild</a></h2>
Tigerchild is especially good on medical issues, tackling
                    over 90 common childhood healthcare problems. Education and
                    general parenting skills are also covered.
        <h2><a href="http://www.travellingwithchildren.co.uk">Travelling with
                    Children</a></h2>
Strategies and solutions based on the experiences of one
                    family and their friends.
        <h2><a href="http://www.twin2twin.org">Twin2Twin</a></h2>
UK Twin to Twin Transfusion Syndrome Association made up
                    of parents, professional carers and researchers.
        <h2><a href="http://www.twinsclub.co.uk">TwinsClub</a></h2>
UK based multiples website for parents and parents-to-be
                      of twins, triplets, quads and more !!
        <h2><a href="http://www.twinslist.org">TwinsList</a></h2>
Interesting and useful information about twins and more.
<h2><a href="http://www.twinsmagazine.com">Twins Magazine</a></h2>
Information resources for parents of twins, triplets, and
                    more. Sample articles. Subscriptions, books and special gifts
                    for families with multiples.
<h2><a href="http://www.twinsonline.org.uk">Twins OnLine</a></h2>
 For parents of twins and twins themselves, aimed mainly
                    at those who have twins as first time parents!
<h2><a href="http://www.ukparents.co.uk">UK Parents</a></h2>
Excellent community site to catch up with other parents
                    and to chat over problems. Ther's even a section specifically
                    for Dads!
<p>If you come across any other useful links on your web travels,
                please let us know.</p>
