    <h1 class="header">Tips - Feeding</h1>
<p>Feed one baby per side - don't bother swapping to avoid
                    confusion. Alternatively a band on your wrist may help you
                    remember who was fed from which side.</p>
<p><b>Simpsons drop-in breastfeeding clinic</b> - every Tuesday afternoon 12noon till 4pm Simpson Centre for Reproductive Health, Ground Floor, near maternity clinics reception desk.  More specialist twin breastfeeding advice than you'll probably get at your local health centre.  Bring your twin pillow if you have one, but there is one there if you want to try one out.  Dads or other supporters welcome to come.  Prams are tricky as the room is very small - use baby carriers or bring car seats where possible.  People from West Lothian are welcome to use this service, too.</p>
<p><b>Special breastfeeding class for twins</b> - First Monday of every month 1.30 - 3pm Simpson Centre for Reproductive Health, Ground Floor, near maternity clinics reception desk.  This is meant to be in addition to your local antenatal class on breastfeeding.</p>
<p>When introducing formula mix it with equal quantities of expressed
                  milk as this will ease the transition for the babies and avoid
                  your breasts becoming engorged.</p>
<p>Bottle Feeding - 2 kettles are useful in case one breaks down;
                  it also leaves one for other things.</p>
<p>Be brave - to heat up milk use your microwave. Once you've
                  tried it you'll wonder how you lived without it.</p>
<p>Wash, sterilise and fill the bottle with boiled water, 1.5
                  ounces less than the right measure and put in the fridge. When
                  you need the bottle, just add the small amount of boiling water
                  you need to make the full measure, add the milk formula, shake
                  and it's ready. You've gained some more minutes, the temperature
                  is perfect and you don't need wait until it cools down or heats
                  up. Sometimes with crying babies 1 minute seems like 1 hour!!</p>
